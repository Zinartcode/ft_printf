#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	int i;

	i = 0;
	ft_strclr(dest);
	while (src[i])
	{
		dest[i] = (char)src[i];
		i++;
	}
	return (dest);
}
