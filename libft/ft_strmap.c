#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	unsigned int	i;
	char			*s2;

	i = 0;
	s2 = NULL;
	if (s && f)
	{
		s2 = ft_strdup(s);
		while (s[i])
		{
			s2[i] = f((char)s[i]);
			i++;
		}
	}
	return (s2);
}
